/*
** É possível utilizar um if sozinho
** Sempre que o else if for usado haverá um if antes
** Podem existir vários else if na checagem
** Só pode existir um único else na checagem
*/

const tenhoDinheiro = false

if (tenhoDinheiro == true) {
    console.log('Tenho dinheiro para sair.')
} else {
    console.log('Não tenho dinheiro para sair.')
}
