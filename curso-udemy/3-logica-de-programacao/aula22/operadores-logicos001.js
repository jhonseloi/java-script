/*
Operadores lógicos:
&& -> TRUE (E) -> Todos os valores precisam ser verdadeiros para retornar true
|| -> OR (OU) -> Apenas um dos valores precisa ser verdadeiro para retornar true
! -> NOT (NÃO/NEGAÇÃO)
*/

// Operador && (E)
console.log(true && true) // true
console.log(true && false) // false
console.log(false && true) // false
console.log(false && false) // false
