// For clássico -> Geralmente utilizado em objetos iteráveis (arrays ou strings)
// i = index, geralmente é o nome atribuído à variável utilizada em for

for (let i = 0; i <= 5; i++) { // Repetição com incremento simples
    console.log(`Linha ${i}`)
}

for (let i = 10; i <= 50; i += 10) { // Repetição com incremento decimal
    console.log(`Linha ${i}`)
}

for (let i = 50; i >= 0; i -= 10) { // Repetição com decremento
    console.log(`Linha ${i}`)
}
